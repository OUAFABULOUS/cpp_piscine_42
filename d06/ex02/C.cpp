#include "C.hpp"

/*
** ------------------------------- CONSTRUCTOR --------------------------------
*/

C::C() : _name("C")
{
}

/*
** -------------------------------- DESTRUCTOR --------------------------------
*/

C::~C()
{
}

/*
** --------------------------------- OVERLOAD ---------------------------------
*/

std::ostream &			operator<<( std::ostream & o, C const & i )
{
	o << "Type = " << i.getName() << std::endl;
	return o;
}


/*
** --------------------------------- METHODS ----------------------------------
*/


/*
** --------------------------------- ACCESSOR ---------------------------------
*/

std::string	C::getName() const {
	return (_name);
}

